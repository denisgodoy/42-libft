char	*ft_itoa(int	n)
{
	int		i;
	char	str[100];
	char	numtos;

	i = 0;
	while (n > 0)
	{
		numtos = n % 10 + '0';
		n = n / 10;
		str[i] = numtos;
		i++;
	}
	str[i] = '\0';
	return (str);
}
